<h2> Variáveis </h2>
<p>
    São estruturas (memória) utilizadas para armazenar
    dados que serão processados pelo programa
</p>

<p>
    Para criar uma variável em PHP basta colocar simbolo de $ na
    frente de um nome e atribuir um valor com o simbolo de igual. Exemplo:

    <pre>
        $variavel = 10;
        $nome = "Maria";
        $nota = 5.5;<>
    </pre>

</p>

<?php
    $nome = "Débora";
    echo "Olá $nome, seja bem vindo a aula de backend";
?>
